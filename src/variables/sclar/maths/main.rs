fn main() {
    // addition
    let sum = 5+7;
    println!("Sum : {}",sum);

    // subtraction
    let deference = 47.5-3.7;
    println!("Subtraction : {}",deference);

    // multiplication
    let product = 5*35;
    println!("Multiplication : {}",product);

    // division
    let quotient = 56.7 / 32.2;
    let truncated = -5/3;
    println!("Quotient : {}",quotient);
    println!("Truncated : {}",truncated);

    // remainder
    let _remainder = 45%7;
    println!("Remainder : {} ",_remainder)
}